def launch_async_epoch_eval(
    epoch: int,
    exp_dir: str,
    logger: logging.Logger,
    state: Optional[Dict] = None,
    cfg: Optional[Dict] = None,
) -> Dict:
    """
    在每个 epoch 结束后后台启动一次 REAL-T 全量评估。

    特性：
      - 仅建议在 rank0 调用
      - 默认若上一轮评估仍在运行，则跳过本轮，避免评估堆积
      - 评估完成后，将 output 目录下生成的 *_summary.txt 复制到
        <exp_dir>/eval_summaries/ 中，按 epoch 归档，避免后续覆盖
    """
    state = dict(state or {})
    cfg = cfg or {}

    enabled = cfg.get("epoch_eval_enabled", True)
    if not enabled:
        return state

    prev_proc = state.get("proc")
    skip_if_running = cfg.get("epoch_eval_skip_if_running", True)
    if prev_proc is not None and prev_proc.poll() is None:
        prev_epoch = state.get("epoch")
        prev_log = state.get("log_path")
        if skip_if_running:
            logger.info(
                f"[Eval] 跳过 epoch={epoch}：上一轮后台评估仍在运行 "
                f"(epoch={prev_epoch}, pid={prev_proc.pid}, log={prev_log})"
            )
            return state

    exp_dir_path = Path(exp_dir).resolve()
    real_t_root = (_SCRIPT_DIR.parent / "REAL-TSE-Challenge").resolve()
    output_root = Path(
        cfg.get("epoch_eval_output_root", str(real_t_root / "output"))
    ).resolve()
    test_set = str(cfg.get("epoch_eval_test_set", "DEV")).upper()
    cuda_id = str(cfg.get("epoch_eval_cuda", "2"))
    fast_flag = bool(cfg.get("epoch_eval_fast", True))

    model_label = exp_dir_path.name
    output_dir = output_root / test_set / model_label
    eval_logs_dir = exp_dir_path / "eval_logs"
    eval_summaries_dir = exp_dir_path / "eval_summaries"
    eval_logs_dir.mkdir(parents=True, exist_ok=True)
    eval_summaries_dir.mkdir(parents=True, exist_ok=True)

    log_path = eval_logs_dir / f"eval_epoch{epoch:03d}.log"
    fast_arg = "--fast " if fast_flag else ""

    cmd = f"""set -euo pipefail
cd {shlex.quote(str(real_t_root))}
bash ./run_all_eval.sh {fast_arg}--model {shlex.quote(str(exp_dir_path))} --test-set {shlex.quote(test_set)} --cuda {shlex.quote(cuda_id)}
summary_file=$(find {shlex.quote(str(output_dir))} -maxdepth 1 -type f -name '*_summary.txt' | sort | head -n 1 || true)
if [ -n "$summary_file" ] && [ -f "$summary_file" ]; then
    summary_base=$(basename "$summary_file")
    cp "$summary_file" {shlex.quote(str(eval_summaries_dir))}/epoch_{epoch:03d}_${{summary_base}}
fi
"""

    with open(log_path, "a", encoding="utf-8") as log_f:
        log_f.write(
            f"\n========== epoch {epoch} eval start: {datetime.datetime.now():%Y-%m-%d %H:%M:%S} =========="
            f"\nmodel={exp_dir_path}\ntest_set={test_set}\ncuda={cuda_id}\noutput_dir={output_dir}\n\n"
        )

    proc = subprocess.Popen(
        ["bash", "-lc", cmd],
        stdout=open(log_path, "a", encoding="utf-8"),
        stderr=subprocess.STDOUT,
        start_new_session=True,
    )
    logger.info(
        f"[Eval] 后台启动 epoch={epoch} 评估, pid={proc.pid}, "
        f"output_dir={output_dir}, log={log_path}"
    )
    logger.info(
        f"[Eval] 本轮完成后会归档 summary 到: {eval_summaries_dir}"
    )
    return {
        "proc": proc,
        "epoch": epoch,
        "log_path": str(log_path),
        "output_dir": str(output_dir),
        "summary_dir": str(eval_summaries_dir),
    }


