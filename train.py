class ExponentialDecayScheduler:
    """
    指数衰减学习率调度器（与 wesep 中保持一致）。

    warm_up_epoch 轮内从 initial_lr 线性预热（或从 0 开始），
    之后按指数从 initial_lr 衰减到 final_lr。
    """

    def __init__(
        self,
        optimizer: torch.optim.Optimizer,
        initial_lr: float,
        final_lr: float,
        num_epochs: int,
        epoch_iter: int,
        warm_up_epoch: int = 1,
        warm_from_zero: bool = False,
    ):
        self.optimizer      = optimizer
        self.initial_lr     = initial_lr
        self.final_lr       = final_lr
        self.num_epochs     = num_epochs
        self.epoch_iter     = epoch_iter
        self.warm_up_epoch  = warm_up_epoch
        self.warm_from_zero = warm_from_zero
        self.total_steps    = num_epochs * epoch_iter
        self.warm_steps     = warm_up_epoch * epoch_iter
        self._step          = 0

    def step(self):
        self._step += 1
        lr = self._get_lr()
        for pg in self.optimizer.param_groups:
            pg["lr"] = lr

    def _get_lr(self) -> float:
        if self._step <= self.warm_steps and self.warm_steps > 0:
            start = 0.0 if self.warm_from_zero else self.initial_lr
            frac  = self._step / self.warm_steps
            return start + frac * (self.initial_lr - start)

        # 指数衰减阶段
        decay_steps = self.total_steps - self.warm_steps
        step_in_decay = self._step - self.warm_steps
        if decay_steps <= 0:
            return self.final_lr
        frac = step_in_decay / decay_steps
        # log-linear 插值
        lr = self.initial_lr * (self.final_lr / self.initial_lr) ** frac
        return float(np.clip(lr, self.final_lr, self.initial_lr))

    def get_last_lr(self) -> float:
        return self._get_lr()

    def state_dict(self) -> Dict:
        return {"_step": self._step}

    def load_state_dict(self, d: Dict):
        self._step = d["_step"]


