def _poly1d_torch(coefficients, x: torch.Tensor) -> torch.Tensor:
    result = torch.zeros_like(x)
    for c in coefficients:
        result = result * x + c
    return result


def _dnsmos_polyfit(sig: torch.Tensor, bak: torch.Tensor, ovr: torch.Tensor):
    """Mirror dnsmos_local._get_polyfit_val (non-personalized)"""
    sig_out = _poly1d_torch([-0.08397278, 1.22083953,  0.0052439],   sig)
    bak_out = _poly1d_torch([-0.13166888, 1.60915514, -0.39604546],  bak)
    ovr_out = _poly1d_torch([-0.06766283, 1.11546468,  0.04602535],  ovr)
    return sig_out, bak_out, ovr_out


class DNSMOSLoss(nn.Module):
    """Differentiable DNSMOS Loss — loads pre-converted .pt models directly, no onnx2torch/wesep/numba dependency.

    Usage:
        loss_fn = DNSMOSLoss(primary_pt, p808_pt, target_metric="OVRL")
        loss = loss_fn(inf=tse_out).mean()   # ref is optional, not used in computation

    .pt files are generated by DNSMOS/convert_onnx_to_pt.py.
    """

    _SR = 16000
    _INPUT_LEN = 9.01  # Consistent with Microsoft DNS-Challenge (seconds)

    def __init__(
        self,
        primary_model_path: str,
        p808_model_path: str,
        target_metric: str = "OVRL",
    ):
        super().__init__()
        assert target_metric in ("OVRL", "SIG", "BAK", "P808"), (
            f"target_metric 必须是 OVRL/SIG/BAK/P808，得到 {target_metric!r}"
        )
        self.target_metric = target_metric
        self._len_samples = int(self._INPUT_LEN * self._SR)

        # 加载预转换的 PyTorch 模型（torch.save 保存的完整模型对象）
        self.primary_model = torch.load(primary_model_path, weights_only=False)
        self.primary_model.eval()
        self.p808_model = torch.load(p808_model_path, weights_only=False)
        self.p808_model.eval()

        self._spectrogram = torchaudio.transforms.Spectrogram(
            n_fft=321, hop_length=160, pad_mode="constant"
        )
        self._to_db = torchaudio.transforms.AmplitudeToDB("power", top_db=80.0)

    def _melspec(self, audio: torch.Tensor) -> torch.Tensor:
        """audio: (T,) → mel (T', 120)"""
        import librosa
        import math
        fb = torch.as_tensor(
            librosa.filters.mel(sr=self._SR, n_fft=321, n_mels=120).T,
            dtype=audio.dtype, device=audio.device,
        )
        spec = self._spectrogram.to(audio.device)(audio.unsqueeze(0)).squeeze(0)  # (161, T')
        mel = torch.matmul(spec.transpose(0, 1), fb)                               # (T', 120)
        self._to_db.db_multiplier = math.log10(
            max(self._to_db.amin, float(mel.max()))
        )
        mel_db = (self._to_db(mel.transpose(0, 1)).transpose(0, 1) + 40.0) / 40.0
        return mel_db

    def _score_one(self, audio: torch.Tensor) -> torch.Tensor:
        """audio: (T,) → scalar DNSMOS score（可微分）"""
        import math
        # 短音频重复填充到至少 9.01s
        while audio.shape[0] < self._len_samples:
            audio = torch.cat([audio, audio], dim=0)

        num_hops = int(math.floor(audio.shape[0] / self._SR) - self._INPUT_LEN) + 1
        hop = self._SR
        scores: List[torch.Tensor] = []

        pm = self.primary_model.to(audio.device)
        p8 = self.p808_model.to(audio.device)

        for idx in range(num_hops):
            seg = audio[int(idx * hop): int((idx + self._INPUT_LEN) * hop)]
            if seg.shape[0] < self._len_samples:
                continue
            inp = seg.float().unsqueeze(0)              # (1, T)
            raw = pm(inp)[0]
            if raw.dim() == 2:
                raw = raw[0]
            sig_r, bak_r, ovr_r = raw[0], raw[1], raw[2]
            sig, bak, ovr = _dnsmos_polyfit(sig_r, bak_r, ovr_r)

            if self.target_metric == "P808":
                mel = self._melspec(seg[:-160])
                p808_score = p8(mel.float().unsqueeze(0))
                if p808_score.dim() > 1:
                    p808_score = p808_score.squeeze()
                scores.append(p808_score)
            else:
                scores.append({"SIG": sig, "BAK": bak, "OVRL": ovr}[self.target_metric])

        if not scores:
            return torch.zeros(1, device=audio.device, requires_grad=True).squeeze()
        return torch.stack(scores).mean()

    def forward(self, inf: torch.Tensor, ref: Optional[torch.Tensor] = None) -> torch.Tensor:
        """
        inf: (B, T)  TSE 输出（ref 不参与计算，可不传）
        返回: (B,) 负 DNSMOS 分数（minimize = maximize DNSMOS）
        """
        if inf.dim() == 3:
            inf = inf[:, 0, :]
        losses: List[torch.Tensor] = []
        for i in range(inf.shape[0]):
            losses.append(-self._score_one(inf[i]))
        return torch.stack(losses)


