def run_tse_forward(
    tse_model: nn.Module,
    mixture: torch.Tensor,     # [B, T]
    enroll: torch.Tensor,      # [B, T_e]
) -> torch.Tensor:
    """
    调用 TSE 模型提取目标说话人音频。
    兼容 wesep BSRNN（旧版）和 TSE_BSRNN_SPK（新版）两种接口。

    返回: tse_out [B, T]（与 mixture 长度相同）
    """
    out = tse_model(mixture, enroll)

    # 处理不同返回格式
    if isinstance(out, (tuple, list)):
        tse_out = out[0]  # 第一个输出为分离音频
    else:
        tse_out = out

    # 确保 shape 为 [B, T]
    if tse_out.dim() == 3:
        tse_out = tse_out.squeeze(1)   # [B, 1, T] -> [B, T]

    return tse_out


def train_one_epoch(
    tse_model:    nn.Module,
    asr_model:    Optional[FrozenWhisperASR],
    dataloader:   DataLoader,
    optimizer:    torch.optim.Optimizer,
    scheduler:    ExponentialDecayScheduler,
    device:       torch.device,
    epoch:        int,
    global_step:  int,
    cfg:          Dict,
    writer:       Optional[SummaryWriter],
    logger:       logging.Logger,
    spk_loss_fn:   Optional[SpeakerSimilarityLoss] = None,
    vad_loss_fn:   Optional[TargetVADLoss] = None,
    dnsmos_loss_fn = None,
    is_main:       bool = True,
    world_size:   int = 1,
) -> Tuple[float, int]:
    """
    单 epoch 训练。返回 (epoch_loss, global_step)
    多卡：每张卡独立跑 dataloader 的一个分片，loss 通过 all-reduce 同步后 rank0 写日志。
    Checkpoint 仅 rank0（is_main=True）写入。
    """
    tse_model.train()

    loss_mode      = cfg.get("loss_mode", "combined")
    lambda_ce      = cfg.get("lambda_ce",      1.0)
    lambda_sim     = cfg.get("lambda_sim",     0.5)
    lambda_vad     = cfg.get("lambda_vad",     0.5)
    lambda_dnsmos  = cfg.get("lambda_dnsmos",  0.0)
    # 兼容旧版 lambda_spk
    if cfg.get("lambda_spk", 0.0) > 0 and loss_mode == "ce":
        loss_mode  = "combined"
        lambda_sim = cfg["lambda_spk"]

    clip_grad      = cfg.get("clip_grad", 5.0)
    log_every      = cfg.get("log_every", 20)
    save_step_int  = cfg.get("save_step_interval", 500)
    max_keep_ckpts = cfg.get("max_keep_ckpts", 3)
    exp_dir        = cfg["exp_dir"]
    model_dir      = Path(exp_dir) / "models"
    asr_language   = cfg.get("asr_language", "zh")

    # 三种 loss 模式校验
    valid_modes = ("ce", "similarity", "combined")
    if loss_mode not in valid_modes:
        raise ValueError(f"[Train] 无效 loss_mode={loss_mode!r}，合法值: {valid_modes}")
    if loss_mode in ("similarity", "combined") and spk_loss_fn is None:
        raise ValueError(
            f"[Train] loss_mode={loss_mode!r} 需要 spk_loss_fn，"
            "请在 config 中配置 spk_encoder_path"
        )
    if loss_mode in ("ce", "combined") and asr_model is None:
        raise ValueError(
            f"[Train] loss_mode={loss_mode!r} 需要 asr_model，"
            "请在 config 中配置 whisper_model_path"
        )

    enable_amp    = cfg.get("enable_amp", False)
    amp_dtype_str = cfg.get("amp_dtype", "float32")
    amp_dtype     = torch.bfloat16 if amp_dtype_str == "bfloat16" else torch.float16
    scaler        = torch.cuda.amp.GradScaler(enabled=enable_amp and amp_dtype != torch.bfloat16)

    # 取 raw module（用于 checkpoint 保存和获取 spk_encoder）
    raw_model = tse_model.module if hasattr(tse_model, "module") else tse_model

    running_loss   = 0.0
    running_ce     = 0.0
    running_sim    = 0.0
    running_vad    = 0.0
    running_dnsmos = 0.0
    n_batches      = 0
    best_loss    = cfg.get("_best_loss", float("inf"))

    # 仅 rank0 显示进度条
    pbar = tqdm(dataloader, desc=f"Epoch {epoch}", dynamic_ncols=True, leave=False,
                disable=not is_main)

    for batch in pbar:
        if batch is None:
            continue

        mixture      = batch["mixture"].to(device)       # [B, T]
        enroll       = batch["enroll"].to(device)        # [B, T_e]
        mix_lens     = batch["mix_lens"].to(device)      # [B]
        transcripts  = batch["transcripts"]              # List[str]
        languages    = batch.get("languages", None)      # List[str] 或 None
        vad_segments = batch.get("vad_segments", None)   # List[Optional[List]]

        optimizer.zero_grad()

        # ── 1. TSE 前向 ─────────────────────────────────────────────────
        with torch.cuda.amp.autocast(enabled=enable_amp, dtype=amp_dtype):
            tse_out = run_tse_forward(tse_model, mixture, enroll)  # [B, T]

        # ── 2. ASR CE Loss ──────────────────────────────────────────────
        ce_loss     = torch.tensor(0.0, device=device)
        sim_loss    = torch.tensor(0.0, device=device)
        vad_loss    = torch.tensor(0.0, device=device)
        dnsmos_loss = torch.tensor(0.0, device=device)
        sim_tse_enroll_mean = torch.tensor(0.0, device=device)
        sim_mix_enroll_mean = torch.tensor(0.0, device=device)

        if loss_mode in ("ce", "combined"):
            # 使用 batch 中每条样本的实际语言（修复中英混合 batch 的语言偏置问题）
            ce_loss = asr_model(
                tse_out,
                transcripts,
                language=asr_language,   # 兜底语言（当 languages 为 None 时使用）
                languages=languages,     # 优先使用逐样本语言
            )

        # ── 3. 说话人相似度 Ranking Loss ────────────────────────────────
        # 目标：sim(tse_out, enroll) > sim(mix, enroll) + margin
        # 按每条样本的语言选择对应编码器（en/chs）
        if loss_mode in ("similarity", "combined") and spk_loss_fn is not None:
            try:
                sim_loss, sim_tse_enroll_mean, sim_mix_enroll_mean = spk_loss_fn(
                    tse_out, mixture, enroll,
                    languages=languages if languages is not None else ["en"] * tse_out.shape[0],
                )
            except Exception as e:
                logger.warning(f"[Train] spk sim loss 计算失败: {e}")
                sim_loss = torch.tensor(0.0, device=device)

        # ── 3.5. 目标说话人活动检测 Loss ─────────────────────────────────
        if loss_mode == "combined" and vad_loss_fn is not None and vad_segments is not None:
            try:
                vad_loss = vad_loss_fn(tse_out, vad_segments, mix_lens)
            except Exception as e:
                logger.warning(f"[Train] VAD loss 计算失败: {e}")
                vad_loss = torch.tensor(0.0, device=device)

        # ── 3.6. 可微分 DNSMOS Loss ─────────────────────────────────────
        # 不需要参考音：DNSMOSLoss.forward 中 ref 仅接口对齐，不参与 DNSMOS 计算
        if loss_mode == "combined" and dnsmos_loss_fn is not None and lambda_dnsmos > 0:
            try:
                dnsmos_loss = dnsmos_loss_fn(inf=tse_out).mean()
            except Exception as e:
                logger.warning(f"[Train] DNSMOS loss 计算失败: {e}")
                dnsmos_loss = torch.tensor(0.0, device=device)

        # ── 4. 合并 Loss ────────────────────────────────────────────────
        if loss_mode == "ce":
            loss = lambda_ce * ce_loss
        elif loss_mode == "similarity":
            loss = lambda_sim * sim_loss
        else:  # combined
            loss = (lambda_ce    * ce_loss
                  + lambda_sim   * sim_loss
                  + lambda_vad   * vad_loss
                  + lambda_dnsmos * dnsmos_loss)

        if not torch.isfinite(loss):
            if is_main:
                logger.warning(f"[Train] step={global_step} loss 非有限值，跳过")
            optimizer.zero_grad()
            global_step += 1
            scheduler.step()
            continue

        # ── 5. 反向传播 ─────────────────────────────────────────────────
        if enable_amp and amp_dtype != torch.bfloat16:
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            nn.utils.clip_grad_norm_(tse_model.parameters(), clip_grad)
            scaler.step(optimizer)
            scaler.update()
        else:
            loss.backward()
            nn.utils.clip_grad_norm_(tse_model.parameters(), clip_grad)
            optimizer.step()

        scheduler.step()
        global_step += 1

        # ── 6. 跨卡 loss 同步（用于日志，不影响梯度）────────────────────
        loss_t    = reduce_mean(loss.detach())
        ce_t      = reduce_mean(ce_loss.detach() if torch.is_tensor(ce_loss) else
                                torch.tensor(ce_loss, device=device))
        sim_t     = reduce_mean(sim_loss.detach() if torch.is_tensor(sim_loss) else
                                torch.tensor(sim_loss, device=device))
        vad_t     = reduce_mean(vad_loss.detach() if torch.is_tensor(vad_loss) else
                                torch.tensor(vad_loss, device=device))
        dnsmos_t  = reduce_mean(dnsmos_loss.detach() if torch.is_tensor(dnsmos_loss) else
                                torch.tensor(dnsmos_loss, device=device))
        sim_tse_t = reduce_mean(sim_tse_enroll_mean.detach()
                                if torch.is_tensor(sim_tse_enroll_mean) else
                                torch.tensor(sim_tse_enroll_mean, device=device))
        sim_mix_t = reduce_mean(sim_mix_enroll_mean.detach()
                                if torch.is_tensor(sim_mix_enroll_mean) else
                                torch.tensor(sim_mix_enroll_mean, device=device))

        loss_val    = loss_t.item()
        ce_val      = ce_t.item()
        sim_val     = sim_t.item()
        vad_val       = vad_t.item()
        dnsmos_val    = dnsmos_t.item()
        sim_tse_val   = sim_tse_t.item()
        sim_mix_val   = sim_mix_t.item()
        running_loss   += loss_val
        running_ce     += ce_val
        running_sim    += sim_val
        running_vad    += vad_val
        running_dnsmos += dnsmos_val
        n_batches      += 1

        current_lr = scheduler.get_last_lr()
        if is_main:
            postfix = {"loss": f"{loss_val:.4f}", "lr": f"{current_lr:.2e}"}
            if loss_mode in ("ce", "combined"):
                postfix["ce"] = f"{ce_val:.4f}"
            if loss_mode in ("similarity", "combined"):
                postfix["sim"] = f"{sim_val:.4f}"
            if loss_mode == "combined" and vad_loss_fn is not None:
                postfix["vad"] = f"{vad_val:.4f}"
            if loss_mode == "combined" and dnsmos_loss_fn is not None and lambda_dnsmos > 0:
                postfix["dnsmos_loss"] = f"{dnsmos_val:.4f}"  # 负值，越接近 0 越好
            pbar.set_postfix(postfix)

        if global_step % log_every == 0 and is_main:
            avg_loss   = running_loss   / max(n_batches, 1)
            avg_ce     = running_ce     / max(n_batches, 1)
            avg_sim    = running_sim    / max(n_batches, 1)
            avg_vad    = running_vad    / max(n_batches, 1)
            avg_dnsmos = running_dnsmos / max(n_batches, 1)
            log_parts = (
                f"[Train] epoch={epoch} step={global_step} loss={avg_loss:.4f}"
            )
            if loss_mode in ("ce", "combined"):
                log_parts += f" ce={avg_ce:.4f}"
            if loss_mode in ("similarity", "combined"):
                log_parts += (
                    f" sim={avg_sim:.4f}"
                    f" sim_tse={sim_tse_val:.3f}"
                    f" sim_mix={sim_mix_val:.3f}"
                )
            if loss_mode == "combined" and vad_loss_fn is not None:
                log_parts += f" vad={avg_vad:.4f}"
            if loss_mode == "combined" and dnsmos_loss_fn is not None and lambda_dnsmos > 0:
                log_parts += f" dnsmos_loss={avg_dnsmos:.4f}"
            log_parts += f" lr={current_lr:.2e}"
            logger.info(log_parts)
            if writer is not None:
                writer.add_scalar("train/loss",           avg_loss,       global_step)
                writer.add_scalar("train/ce_loss",        avg_ce,         global_step)
                writer.add_scalar("train/sim_loss",       avg_sim,        global_step)
                writer.add_scalar("train/vad_loss",       avg_vad,        global_step)
                writer.add_scalar("train/dnsmos_loss",    avg_dnsmos,     global_step)
                writer.add_scalar("train/sim_tse_enroll", sim_tse_val,    global_step)
                writer.add_scalar("train/sim_mix_enroll", sim_mix_val,    global_step)
                writer.add_scalar("train/lr",             current_lr,     global_step)
            running_loss = running_ce = running_sim = running_vad = running_dnsmos = 0.0
            n_batches = 0

        # ── 7. 定期保存 checkpoint（仅 rank0）─────────────────────────
        if global_step % save_step_int == 0 and is_main:
            ckpt_path = model_dir / f"checkpoint_step{global_step:07d}.pt"
            save_checkpoint(
                str(ckpt_path),
                step=global_step,
                epoch=epoch,
                tse_model=raw_model,      # 保存 raw module，不含 DDP 包装
                optimizer=optimizer,
                scheduler=scheduler,
                best_loss=best_loss,
            )
            latest = model_dir / "latest_checkpoint.pt"
            if latest.is_symlink() or latest.exists():
                latest.unlink()
            latest.symlink_to(ckpt_path.name)
            rotate_checkpoints(str(model_dir), max_keep_ckpts)
            logger.info(f"[Ckpt] 保存 step={global_step}: {ckpt_path.name}")

    epoch_loss = running_loss / max(n_batches, 1)
    return epoch_loss, global_step


